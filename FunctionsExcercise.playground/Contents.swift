import UIKit


// Adding

func add(one: Double, two: Double) {
    one + two
}

add(1, two: 3.13)


// Subtraction

func subtract(numberOne: Int, numberTwo: Int) {
    numberOne - numberTwo
}

subtract(4, numberTwo: 1)


// Multiplication

func multiply(numberOne: Float, numberTwo: Float) {
    numberOne * numberTwo
}

multiply(3, numberTwo: 4.1)


// Division

func divide(numberOne: Double, numberTwo: Double) {
    numberOne / numberTwo
}

divide(15, numberTwo: 2)

